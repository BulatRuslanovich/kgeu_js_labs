// Форматирует число, добавляя разделители тысяч запятыми
function formatNumber(num) {
  // /\d(?=(\d{3})+(?!\d))/g:
  //   \d                     — берём текущую цифру;
  //   (?=(\d{3})+(?!\d))      — положительный просмотр вперёд:
  //                            • (\d{3})+  — одна или более групп по три цифры подряд;
  //                            • (?!\d)    — сразу после последней тройки не должно быть новой цифры,
  //                                          то есть справа располагается конец числа (точка, запятая, конец строки и т.п.);
  //   g                       — обрабатываем каждую такую позицию.
  // Замена "$&," добавляет запятую сразу после найденной цифры.
  return num.toString().replace(/\d(?=(\d{3})+(?!\d))/g, "$&,");
}

export function runTask() {
  const samples = [1234, 12345.67, 123456, 1234567.89];
  return samples.map((value) => `${value} -> ${formatNumber(value)}`);
}
