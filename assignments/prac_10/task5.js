// INFO: Проверяет, лежит ли год в диапазоне 1500-1800 включительно
// ^(1[5-7][0-9]{2}|1800)$:
//   ^                 — начало строки, запрещаем лишние символы перед годом;
//   1[5-7][0-9]{2}    — числа 1500-1799: первая цифра 1, затем 5-7, далее две любые цифры;
//   |1800             — отдельное разрешённое значение 1800;
//   $                 — конец строки, чтобы не было лишних символов.
const rgx = /^(1[5-7][0-9]{2}|1800)$/;

export function runTask() {
  const samples = ["1500", "1800", "1499", "1701"];
  return samples.map((sample) => `${sample}: ${rgx.test(sample)}`);
}
