// Проверяет соответствие строк формату времени hh.mm am/pm
// ^(0[0-9]|1[0-2])\.[0-5][0-9] (am|pm)$:
//   ^              — начало строки, не допускаем лидирующие символы;
//   (0[0-9]|1[0-2]) — часы: 00-09 или 10-12;
//   \.             — точка-разделитель между часами и минутами;
//   [0-5][0-9]     — минуты: первая цифра 0-5, вторая 0-9;
//   (am|pm)        — обязательный указатель времени в нижнем регистре;
//   $              — конец строки, исключает хвостовые символы.
const rgx = /^(0[0-9]|1[0-2])\.[0-5][0-9] (am|pm)$/;

export function runTask() {
  const samples = ["11.59 am", "00.01 pm", "12.00 am"];
  return samples.map((sample) => `${sample}: ${rgx.test(sample)}`);
}
