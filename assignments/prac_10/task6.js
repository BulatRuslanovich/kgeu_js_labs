// INFO: Находит слова, начинающиеся и заканчивающиеся одинаковой буквой
const str = "push pop avada kedavra";
// /\b(\w)(\w*?\1)?\b/gi:
//   \b          — слово должно начинаться на границе слова;
//   (\w)        — первая группа запоминает первый символ слова;
//   (\w*?\1)?   — необязательная группа: любое количество символов (ленивый режим),
//                 после которых повторяется захваченный первой группой символ;
//   \b          — слово заканчивается на границе;
//   g           — ищем все подходящие слова;
//   i           — регистронезависимый поиск.
const pattern = /\b(\w)(\w*?\1)?\b/gi;

function findWords(input) {
  return input.match(pattern) ?? [];
}

export function runTask() {
  const words = findWords(str);
  return [`Строка: "${str}"`, `Подходящие слова: ${words.join(", ")}`];
}
